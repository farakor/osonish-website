/**
 * Переводит название категории на указанный язык
 * @param categoryName - Название категории на русском
 * @param locale - Локаль (ru или uz)
 * @returns Переведенное название категории
 */
export function translateCategory(categoryName: string, locale: string): string {
  const translation = categoryTranslations[categoryName];
  
  if (!translation) {
    // Если перевод не найден, возвращаем оригинальное название
    return categoryName;
  }
  
  return locale === 'uz' ? translation.uz : translation.ru;
}

/**
 * Форматирует количество вакансий с правильным окончанием
 * @param count - Количество вакансий
 * @param locale - Локаль (ru или uz)
 * @returns Отформатированная строка
 */
export function formatVacancyCount(count: number, locale: string): string {
  if (locale === 'uz') {
    return `${count} vakansiya`;
  }
  
  // Для русского языка
  const lastDigit = count % 10;
  const lastTwoDigits = count % 100;
  
  if (lastTwoDigits >= 11 && lastTwoDigits <= 19) {
    return `${count} вакансий`;
  }
  
  if (lastDigit === 1) {
    return `${count} вакансия`;
  }
  
  if (lastDigit >= 2 && lastDigit <= 4) {
    return `${count} вакансии`;
  }
  
  return `${count} вакансий`;
}


